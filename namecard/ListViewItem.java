package com.example.namecard;

public class ListViewItem {
    private String txtItemName;
    private String txtItemTel;
    private String txtItemEmail;

    public String getTxtItemName() {return txtItemName;}

    public void setTxtItemName(String txtItemName) {
        this.txtItemName = txtItemName;
    }

    public String getTxtItemTel() {
        return txtItemTel;
    }

    public void setTxtItemTel(String txtItemTel) {
        this.txtItemTel = txtItemTel;
    }

    public String getTxtItemEmail() {
        return txtItemEmail;
    }

    public void setTxtItemEmail(String txtItemEmail) {
        this.txtItemEmail = txtItemEmail;
    }
}
